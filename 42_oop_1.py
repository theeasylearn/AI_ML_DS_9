#create class
class Human:
    def walk(self):
        print("I can walk")
    def talk(self):
        print("I can talk")
    def sleep(self):
        print("I can sleep")

#create object
ronit = Human() 
ronit.walk()
ronit.talk()
ronit.sleep()

#create another object
ankit = Human()
ankit.sleep()
ankit.walk()
ankit.talk()

